# Integer
Age = 40
print("Age(int):", Age)

# Float
BGL = 6.9
print("BGL(float):", BGL)

# String
name = "Ramesh"
print("name(string):", name)

# List
fruits = ["apple", "orange", "cherry"]
print("List of fruits:", fruits)

# Dictionary
person = {
    "name": name,
    "age": Age,
    "BGL": BGL,
    "city": "Bilaspur",
    "Fruit":fruits[0]
}
print("Dictionary (person):", person)

